<script>
document.addEventListener('DOMContentLoaded', function() {
    const popup = document.querySelector('.comp-105-popup');
    const closeBtn = document.querySelector('.popup-close');
    const selectedFilesInput = document.getElementById('selected_files_input');
    let selectedFiles = [];
    
    // Функция открытия попапа с выбранными файлами
    window.openDownloadPopup = function(files) {
        selectedFiles = files;
        selectedFilesInput.value = JSON.stringify(files);
        popup.style.display = 'flex';
        document.body.style.overflow = 'hidden';
    };
    
    // Закрытие попапа
    closeBtn.addEventListener('click', closePopup);
    
    // Закрытие по клику на фон
    popup.addEventListener('click', function(e) {
        if (e.target === popup) {
            closePopup();
        }
    });
    
    function closePopup() {
        popup.style.display = 'none';
        document.body.style.overflow = '';
    }
    
    // Модифицируем обработку формы Gravity Forms
    const gform = document.getElementById('gform_7');
    if (gform) {
        gform.addEventListener('submit', function(e) {
            // Добавляем небольшую задержку для обработки формы
            setTimeout(function() {
                // Если форма успешно отправлена, скачиваем файлы
                if (selectedFiles.length > 0) {
                    downloadSelectedFiles(selectedFiles);
                    selectedFiles = [];
                    closePopup();
                }
            }, 1000);
        });
    }
    
    // Функция скачивания выбранных файлов
    function downloadSelectedFiles(files) {
        files.forEach(fileData => {
            const fileCheckbox = document.querySelector(`input[data-file-id="${fileData.id}"]`);
            if (fileCheckbox) {
                const fileUrl = fileCheckbox.dataset.fileUrl;
                const fileName = fileCheckbox.dataset.fileName;
                
                // Создаем временную ссылку для скачивания
                const link = document.createElement('a');
                link.href = fileUrl;
                link.download = fileName;
                link.style.display = 'none';
                document.body.appendChild(link);
                link.click();
                document.body.removeChild(link);
            }
        });
        
        // Очищаем выбранные файлы в основном интерфейсе
        clearSelectedFiles();
    }
    
    // Функция очистки выбранных файлов в таблице
    function clearSelectedFiles() {
        document.querySelectorAll('.file-checkbox:checked').forEach(checkbox => {
            checkbox.checked = false;
        });
        const selectedCount = document.getElementById('selected-count');
        if (selectedCount) {
            selectedCount.textContent = '0';
        }
    }
    
    // Обработка успешной отправки формы Gravity Forms
    if (typeof gform !== 'undefined') {
        // Перехватываем успешную отправку формы
        const originalGform = window.gform;
        if (originalGform) {
            const originalInit = originalGform.initializeOnLoaded;
            if (originalInit) {
                window.gform.initializeOnLoaded = function(callback) {
                    originalInit.call(this, function() {
                        if (typeof callback === 'function') {
                            callback();
                        }
                        
                        // Добавляем обработчик успешной отправки
                        jQuery(document).on('gform_confirmation_loaded', function(event, formId) {
                            if (formId == 7) {
                                // Форма успешно отправлена - скачиваем файлы
                                if (selectedFiles.length > 0) {
                                    downloadSelectedFiles(selectedFiles);
                                    selectedFiles = [];
                                    closePopup();
                                }
                            }
                        });
                    });
                };
            }
        }
    }
});

